import sys
import os


# ── Scramble Layer ──────────────────────────────────────────────────────────
# Applies a position-dependent byte scramble to prevent frequency analysis.
# Key derivation: k[i] = (i * SCATTER_MUL + SCATTER_OFF) mod 256

SCATTER_MUL = 37
SCATTER_OFF = 13

def _scramble(data: bytes) -> bytes:
    return bytes([b ^ ((i * SCATTER_MUL + SCATTER_OFF) % 256)
                  for i, b in enumerate(data)])


# ── Bit Permutation Layer ────────────────────────────────────────────────────
# Nibble swap followed by a left bit-rotation for additional diffusion.

def _bit_permute(data: bytes) -> bytes:
    out = []
    for b in data:
        s = ((b & 0x0F) << 4) | ((b & 0xF0) >> 4)   # swap nibbles
        r = ((s << 3) | (s >> 5)) & 0xFF              # rotate left 3
        out.append(r)
    return bytes(out)


# ── Archive Framing ──────────────────────────────────────────────────────────
# Wraps the payload in the .ward container format:
#   [MAGIC: 8 bytes][INTEGRITY NONCE: 16 bytes][PAYLOAD][CHECKSUM: 4 bytes]
#
# The integrity nonce is generated by a 64-bit LCG seeded from NONCE_SEED.
# The checksum is a fixed sentinel used by the legacy reader to validate frames.

WARD_MAGIC    = bytes([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0xFF])
WARD_CHECKSUM = bytes([0xCA, 0xFE, 0xBA, 0xBE])
NONCE_SEED    = 0xDEAD
NONCE_LEN     = 16

_LCG_A = 6364136223846793005
_LCG_C = 1442695040888963407
_LCG_M = 2 ** 64

def _lcg_nonce(seed: int = NONCE_SEED, n: int = NONCE_LEN) -> bytes:
    state = seed
    out   = []
    for _ in range(n):
        state = (_LCG_A * state + _LCG_C) % _LCG_M
        out.append(state & 0xFF)
    return bytes(out)

def _frame(payload: bytes) -> bytes:
    return WARD_MAGIC + _lcg_nonce() + payload + WARD_CHECKSUM


# ── Symbol Encoding Layer ────────────────────────────────────────────────────
# The framed binary is encoded into printable ASCII using a proprietary
# base-75 scheme.  The encoding alphabet is derived by shuffling printable
# ASCII with a seeded Mulberry32 PRNG and taking the first 75 characters.
#
# This ensures the .ward file contains only printable characters and cannot
# be trivially decoded with standard base64/base85 tools.

_ALPHA_SEED = 0x539   # DO NOT CHANGE — existing archives will break

def _mulberry32(seed: int):
    def _next():
        nonlocal seed
        seed  = (seed + 0x6D2B79F5) & 0xFFFFFFFF
        t     = seed ^ (seed >> 15)
        t     = (t * (t | 1)) & 0xFFFFFFFF
        t    ^= t + (((t ^ (t >> 7)) | 0x3C6EF35F) & 0xFFFFFFFF)
        return t & 0xFFFFFFFF
    return _next

def _build_alphabet(seed: int = _ALPHA_SEED) -> bytes:
    chars = list(range(0x21, 0x7F))   # 94 printable ASCII characters
    rng   = _mulberry32(seed)
    for i in range(len(chars) - 1, 0, -1):
        j = rng() % (i + 1)
        chars[i], chars[j] = chars[j], chars[i]
    return bytes(chars[:75])

def _encode(data: bytes, alphabet: bytes) -> bytes:
    n       = int.from_bytes(data, 'big')
    digits  = []
    while n:
        digits.append(alphabet[n % 75])
        n //= 75
    leading = len(data) - len(data.lstrip(b'\x00'))
    return bytes([alphabet[0]] * leading + digits[::-1])


# ── Pipeline ─────────────────────────────────────────────────────────────────

def encode(plaintext: bytes) -> bytes:
    step1    = _scramble(plaintext)
    step2    = _bit_permute(step1)
    step3    = _frame(step2)
    alphabet = _build_alphabet()
    step4    = _encode(step3, alphabet)
    return step4


# ── Entry Point ───────────────────────────────────────────────────────────────

if __name__ == '__main__':
    if len(sys.argv) != 3:
        print(__doc__)
        sys.exit(1)

    src, dst = sys.argv[1], sys.argv[2]

    if not os.path.exists(src):
        print(f"[!] Input file not found: {src}")
        sys.exit(1)

    with open(src, 'rb') as f:
        data = f.read()

    encoded = encode(data)

    with open(dst, 'wb') as f:
        f.write(encoded)

    print(f"[+] Encoded {len(data)} bytes → {len(encoded)} chars  ({dst})")
